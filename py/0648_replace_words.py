    def _search(self, trie, word):
        if word[0] not in trie.keys():
            return word
        for char in word:
            if trie.get("final"):
                return trie["final"]
            if char not in trie.keys():
                return word
            trie = trie[char]
            
        return word

        

    def _build_trie(self, roots):
        """
        如果一个 root1 是 root2 的根
        仅存 root1 即可
        """
        roots.sort()
        trie = {}
        for root in roots:
            curr = trie
            reach_final = True
            for char in root:
                if char in trie.keys():
                    # 如果 root1 是 root2 的根，（当前在记录 root2），直接 break
                    reach_final = False
                    break
                curr = curr.setdefault(char, {})
            if reach_final:
                curr.setdefault("final", root)
        return trie


